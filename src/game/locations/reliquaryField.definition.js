function exteriorBlocker(id, minX, maxX, minZ, maxZ, {
  height = 3,
  type = 'exterior',
  tags = [],
  purpose = 'exteriorStructure',
  visualStructureId = id,
  invisible = false,
} = {}) {
  return {
    id,
    type,
    minX,
    maxX,
    minZ,
    maxZ,
    height,
    blocksPlayer: true,
    blocksEnemies: true,
    blocksLineOfMovement: true,
    tags: ['field-collider', ...tags],
    userData: { purpose, visualStructureId, invisible },
  };
}

function visualStructure(id, minX, maxX, minZ, maxZ, {
  kind = 'stoneMass',
  grounded = true,
  y = 0,
} = {}) {
  return {
    id,
    kind,
    bounds: { minX, maxX, minZ, maxZ },
    position: { x: (minX + maxX) / 2, y, z: (minZ + maxZ) / 2 },
    grounded,
  };
}

